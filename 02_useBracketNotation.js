// Access an Array's Contents Using Bracket 
// Notation

/* Each array item has a corresponding index 
starting with zero for the first item. */

let ourArray = ['a', 'b', 'c']

/* Use bracket notation to assign an item of an 
array to a variable or to perform some calculation
on it */

let ourVariable = ourArray[0];
console.log(ourVariable);  // a

/* The value of an array item can be updated 
using bracket notation */
ourArray[2] = 10;
console.log(ourArray);  // ['a', 'b', 10]